std::wstring_view LookupRof2OpcodeName(std::uint32_t opcode) noexcept {
    const auto it = std::lower_bound(
        kRof2OpcodeNames.begin(),
        kRof2OpcodeNames.end(),
        opcode,
        [](const OpcodeNameEntry& entry, std::uint32_t value) {
            return entry.opcode < value;
        });
    if (it == kRof2OpcodeNames.end() || it->opcode != opcode) {
        return L"unknown";
    }
    return it->name;
}

